//--------------------------------------------------------------------
PF_Err DispAngelStr8(AngelWing *wing,char c,A_long x,A_long y)
{
	//STR_START
	if ( ( c<=STR_START)||(c>STR_LAST) ) return PF_Err_NONE;
	
	A_long targetFont = c - STR_START;
	A_long i,j,xx,yy;
	A_long fAdr = 0;
	for ( j=0; j<STR_HEIGHT;j++){
		yy = y+j*DRAWSCALE;
		for ( i=0; i<STR_WIDTH;i++){
			xx = x + i*DRAWSCALE;
			A_u_char v = STR_FONT[targetFont][fAdr];
			if (v>0){
				PF_Pixel8 p = wing->col;
				p.alpha = v;
				wing->scr->setPixelBlend8(xx,yy,p);
				wing->scr->setPixelBlend8(xx+1,yy,p);
				wing->scr->setPixelBlend8(xx,yy+1,p);
				wing->scr->setPixelBlend8(xx+1,yy+1,p);
			}
			fAdr++;
		}
	}

	return PF_Err_NONE;
}
//--------------------------------------------------------------------
PF_Err DispAngel8(AngelWing *wing)
{
	if (wing == NULL) return 0xFF;

	A_long i;
	for ( i=0;i<wing->msgCount;i++){
		DispAngelStr8(wing,wing->msg[i],wing->x+ i * STR_WIDTH * DRAWSCALE, wing->y);
	}

	return PF_Err_NONE;
}
//--------------------------------------------------------------------
PF_Err DispAngel(CFsGraph *scr,char *msg,A_long x, A_long y,PF_Pixel8 col)
{
	AngelWing wing;
	wing.scr		= scr;
	wing.msg		= msg;
	wing.msgCount	= strlen(msg);
	
	if(wing.msgCount<=0) return 0xFF;
	
	wing.x = x;
	wing.y = y;
	
	wing.col = col;
	wing.col16 = CONV8TO16(col);

	wing.msgCountMax= scr->width()/STR_WIDTH;
	if (wing.msgCount>wing.msgCountMax) wing.msgCount = wing.msgCountMax;
	if (scr->is8Bit()){
		return DispAngel8(&wing);
	}

	return PF_Err_NONE;
}

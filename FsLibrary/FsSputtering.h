static void SputDataDraw8(sputDrawPrm *sdp,PF_Pixel col)
{
	PF_Pixel *oD;
	oD = (PF_Pixel *)sdp->data;
	A_long w = sdp->width;
	A_long wt = sdp->widthTrue;
	A_long h = sdp->height;

	PF_Pixel c = col;
	if (sdp->sptIdx>=50){
		if ( (sdp->x>=0)&&(sdp->x<w)&&(sdp->y>=0)&&(sdp->y<h) ){
			c.alpha = RoundByteFpShort(PF_MAX_CHAN8 * sdp->opa);
			A_long adr = sdp->x + sdp->y * wt;
			oD[adr] = PixelBlend8(oD[adr],c);	
		}
		return;
	}

	A_long sptSize = SPT_SIZE(sdp->sptIdx);
	A_long x0 = sdp->x - (sptSize/2);
	A_long y0 = sdp->y - (sptSize/2);

	A_long i,j,x,y;
	for ( j=0;j<sptSize;j++){
		y = y0 + j;
		for ( i=0;i<sptSize;i++){
			x = x0 + i;
			if ( (y>=0)&&(y<h)&&(x>=0)&&(x<w) ) {
				c.alpha = RoundByteFpShort(SPT_VALUE(sdp->sptIdx,i,j) * sdp->opa);
				if (c.alpha>0){
					A_long adr = x + y * wt;
					oD[adr] = PixelBlend8(oD[adr],c);	
				}
			}
		}
	}
}
